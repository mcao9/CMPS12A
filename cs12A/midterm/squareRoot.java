import java.util.Scanner;
import java.lang.Math;

class squareRoot{
   public static void main(String[] args){
     Scanner sc = new Scanner(System.in);
     double x, y;
     x = sc.nextDouble();
     y = sc.nextDouble();
     System.out.println(Math.sqrt(x)+Math.sqrt(y));
  }
}      
